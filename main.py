# 1. Найдите сумму цифр трехзначного числа.

n = int(input("Введите трехзначное число: "))
a = n // 100  # первая цифра числа
b = (n // 10) % 10  # вторая цифра числа
c = n % 10  # третья цифра числа

result = a + b + c
print("Сумма цифр числа:", n, '=', result)

