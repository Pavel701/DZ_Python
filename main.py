#3. Вы пользуетесь общественным транспортом? Вероятно, вы расплачивались за проезд и получали билет с номером.
# Счастливым билетом называют такой билет с шестизначным номером, где сумма первых трех цифр равна сумме последних трех.
# Т.е. билет с номером 385916 – счастливый, т.к. 3+8+5=9+1+6.
# Вам требуется написать программу, которая проверяет счастливость билета.


ticket = input("Введите номер билета (6 цифр): ")

# Проверка длины номера билета
if len(ticket) != 6:
    print("Неверный формат номера билета.")
else:

    sum_first = int(ticket[0]) + int(ticket[1]) + int(ticket[2])
    sum_second = int(ticket[3]) + int(ticket[4]) + int(ticket[5])

    # Проверка
    if sum_first == sum_second:
        print("Билет с номером", ticket, "является счастливым!")
    else:
        print("Билет с номером", ticket, "не является счастливым.")


#4. Требуется определить, можно ли от шоколадки размером n × m долек отломить k долек,
# если разрешается сделать один разлом по прямой между дольками (то есть разломить шоколадку на два прямоугольника).

n = int(input("Введите количество долек по горизонтали (n): "))
m = int(input("Введите количество долек по вертикали (m): "))
k = int(input("Введите количество отломленных долек (k): "))

# Проверяем, что количество отломленных долек не превышает общее количество долек в шоколадке
if k > n * m:
    print("Невозможно отломить", k, "долек от шоколадки размером", n, "×", m)
else:
    # Проверяем, можно ли отломить k долек
    if k % n == 0 or k % m == 0:
        print("Можно отломить", k, "долек от шоколадки размером", n, "×", m)
    else:
        print("Невозможно отломить", k, "долек от шоколадки размером", n, "×", m)