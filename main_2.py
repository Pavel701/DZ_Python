coins = input("Введите последовательность монеток (например, RRRGGG): ")

# Подсчитываем количество монеток, которые нужно перевернуть
count = min(coins.count("R"), coins.count("G"))

print("Минимальное количество монет, которые нужно перевернуть:", count)
