S = int(input("Введите сумму: "))
P = int(input("Введите произведение: "))

for x in range(1, 1001):
    y = S - x
    if x * y == P:
        print("Числа X и Y:", x, y)
        break
else:
    print("Нет таких чисел X и Y.")

